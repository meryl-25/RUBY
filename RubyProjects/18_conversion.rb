# conversion of string to integer
print "Enter your age: " 
age = gets.chomp.to_i  # Converts input to an integer 
puts "In 5 years, you will be #{age + 5} years old." 


# conversion of string to float
print "\nEnter a number: "
num = gets.chomp.to_f  # Converts input to float
puts "Half of your number is #{num / 2}."

