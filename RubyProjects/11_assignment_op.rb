c = 5 

puts "Assignment Operators:"
puts "c = #{c}" 
puts "c += 2  :  #{c += 2}"     # c = c + 2 
puts "c -= 1  :  #{c -= 1}"     # c = c - 1 
puts "c *= 3  :  #{c *= 3}"     # c = c * 3 
puts "c /= 2  :  #{c /= 2}"     # c = c / 2 
puts "c %= 4  :  #{c %= 4}"     # c = c % 4 
puts "c **= 2 :  #{c **= 2}"    # c = c ** 2 